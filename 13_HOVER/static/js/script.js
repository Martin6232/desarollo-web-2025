function hover1(element) {
    let img = element.querySelector('img');
    let span = element.querySelector('span');
    img.remove();
    span.remove();
    document.getElementById('cont').innerHTML = '<video width="150" height="115" controls autoplay src="resources/videos/PREMEDITATED (Full Combo) - FNF Myths of Yamaha.mp4" class="vid-s"></video> <span class="video-title">PREMEDITATED: Myths of Yamaha</span>';
}

function out1(element) {
    let video = element.querySelector('video');
    let span = element.querySelector('span');
    video.remove();
    span.remove();
    document.getElementById('cont').innerHTML = '<img src="resources/img/ZWzXbVEC8fU_1280x720.jpg" class="vid-s" id="video"> <span class="video-title">PREMEDITATED: Myths of Yamaha</span>';
}

